using System;
using System.Collections.Generic;
using System.Text;

namespace HyBy.FrameWork.Common
{
    public class TimeParser
    {
        /// <summary>
        /// 把秒转换成分钟
        /// </summary>
        /// <returns></returns>
        public static int SecondToMinute(int Second)
        {
            decimal mm = (decimal)((decimal)Second / (decimal)60);
            return Convert.ToInt32(Math.Ceiling(mm));           
        }

        #region 返回某年某月最后一天
        /// <summary>
        /// 返回某年某月最后一天
        /// </summary>
        /// <param name="year">年份</param>
        /// <param name="month">月份</param>
        /// <returns>日</returns>
        public static int GetMonthLastDate(int year, int month)
        {
            DateTime lastDay = new DateTime(year, month, new System.Globalization.GregorianCalendar().GetDaysInMonth(year, month));
            int Day = lastDay.Day;
            return Day;
        }
        #endregion

        #region 返回时间差
        public static string DateDiff(DateTime DateTime1, DateTime DateTime2)
        {
            string dateDiff = null;
            try
            {
                //TimeSpan ts1 = new TimeSpan(DateTime1.Ticks);
                //TimeSpan ts2 = new TimeSpan(DateTime2.Ticks);
                //TimeSpan ts = ts1.Subtract(ts2).Duration();
                TimeSpan ts = DateTime2 - DateTime1;
                if (ts.Days >=1)
                {
                    dateDiff = DateTime1.Month.ToString() + "月" + DateTime1.Day.ToString() + "日";
                }
                else
                {
                    if (ts.Hours > 1)
                    {
                        dateDiff = ts.Hours.ToString() + "小时前";
                    }
                    else
                    {
                        dateDiff = ts.Minutes.ToString() + "分钟前";
                    }
                }
            }
            catch
            { }
            return dateDiff;
        }
        #endregion

        #region 检查时间,判断是否年,月第一天或最后一天
        /// <summary>
        /// 是否当月第一天
        /// </summary>
        /// <param name="time"></param>
        /// <returns></returns>
        public Boolean IsMonthBegin(DateTime time)
        {
            if (time.AddDays(-1).Month == time.AddMonths(-1).Month)
            {
                return true;
            }
            else
            {
                return false;
            }
        }
        /// <summary>
        /// 是否当月最后一天
        /// </summary>
        /// <param name="time"></param>
        /// <returns></returns>
        public Boolean IsMonthEnd(DateTime time)
        {
            if (time.AddDays(1).Month == time.AddMonths(1).Month)
            {
                return true;
            }
            else
            {
                return false;
            }
        }
        /// <summary>
        /// 是否当年第一天
        /// </summary>
        /// <param name="time"></param>
        /// <returns></returns>
        public Boolean IsYearBegin(DateTime time)
        {
            if (time.DayOfYear == 1)
            {
                return true;
            }
            else
            {
                return false;
            }
        }
        /// <summary>
        /// 是否当年最后一天
        /// </summary>
        /// <param name="time"></param>
        /// <returns></returns>
        public Boolean IsYearEnd(DateTime time)
        {
            if (time.Month == 12 && IsMonthEnd(time))
            {
                return true;
            }
            else
            {
                return false;
            }
        }
        #endregion
    }
}
